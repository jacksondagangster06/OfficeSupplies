public class State
{
    private String name;
    private String capital;
    private int pop;

public State(String name, String capital, int pop)
{
    this.name = name;
    this.capital = capital;
    this.population = population;
}
    public String getName()
    {
        return name;
    }
    public String getCapital()
    {
        return capital;
    }
    public int getPop()
    {
        return pop;
    }
}